const numbers = [1, 2, 3, 4, 5];
// Use arrow function with map() to double numbers
const doubleNumbers = numbers.map(num => num * 2);
console.log(doubleNumbers); // Output: [2, 4, 6, 8, 10]
// Explanation:
// Use map() to create a new array with each number doubled
// Flowchart Logic:
// Start → Use map() to iterate over numbers array → For each element, multiply by 2 → Return new array → End